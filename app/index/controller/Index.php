    public function jianli()
    {
        if ($this->request->isPost()){
            $data = $this->request->post();
            if (empty($data['mobile'])){
                return $this->error('手机号码未填');
            }
            if (empty($data['attachment'])){
                return $this->error('请上传简历');
            }
            $d = [
                'cid' => $data['cid'],
                'name' => $data['mobile'],
                'job' => $data['job'],
                'mobile' => $data['mobile'],
                'attachment' => $data['attachment'],
                'remark' => $data['remark'],
                'source' => '官网直投',
                'zhaopin_id' => $data['zhaopin_id'],
            ];
            if (!ResumeItem::create($d)){
                return $this->error('提交失败');
            }else{
                return $this->error('提交成功');
            }
        }
        return $this->fetch();
    }

    public function detail1($id)
    {
        $today = date('Y-m-d');
        $where = [
            'id' => $id,
//            'cid' => session('admin_user.cid'),
            'status' => 1,
            'start_time' => ['elt',"{$today}"],
            'end_time' => ['egt',"{$today}"],
        ];
        $row = ShopItem::where($where)->find();
        if (!$row){
            return $this->error('商品不存在');
        }else{
            $startdate=strtotime($row['start_time']);
            $enddate=strtotime($today);
            $days=round(($enddate-$startdate)/3600/24);
            if ($row['time_interval'] != 0){
                $row['score'] += floor($days/$row['time_interval'])*$row['add_score'];
            }
        }
        $row['remark'] = htmlspecialchars_decode($row['remark']);
        $this->assign('data_list', $row);
        return $this->fetch();
    }

